public class InfoBean {

	private double latitue;
	private double longtitue;
	private int imgId;
	private String name;
	private String destence;
	private int zan;

	public double getLatitue() {
		return latitue;
	}

	public void setLatitue(double latitue) {
		this.latitue = latitue;
	}

	public double getLongtitue() {
		return longtitue;
	}

	public void setLongtitue(double longtitue) {
		this.longtitue = longtitue;
	}

	public int getImgId() {
		return imgId;
	}

	public void setImgId(int imgId) {
		this.imgId = imgId;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getDestence() {
		return destence;
	}

	public void setDestence(String destence) {
		this.destence = destence;
	}

	public int getZan() {
		return zan;
	}

	public void setZan(int zan) {
		this.zan = zan;
	}
}
